export const deleteOrder = (id, user) => async (dispatch, getState) => {
  try {
    dispatch({
      type: ORDER_DELETE_REQUEST,
    });

    const {
      userLogin: { userInfo },
    } = getState();

    const config = {
      headers: {
        Authorization: `Bearer ${userInfo.token}`,
      },
    };

    if (userInfo._id === user || userInfo.isAdmin) {
      const { data } = await axios.delete(
        `/api/orders/${id}/${userInfo._id}/${userInfo.isAdmin}`,
        config
      );

      dispatch({
        type: ORDER_DELETE_SUCCESS,
        payload: {
          message: data,
          id,
        },
      });
      toast.success("The order is deleted successfully", {
        position: "top-left",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
      });

      setTimeout(() => {
        dispatch({
          type: ORDER_DELETE_CLEAR_MESSAGE,
        });
      }, 2500);
    } else {
      dispatch({
        type: ORDER_DELETE_FAIL,
        payload: "Not authorized, token failed",
      });
    }
  } catch (error) {
    dispatch({
      type: ORDER_DELETE_FAIL,
      payload:
        error.response && error.response.data.message
          ? error.response.data.message
          : error.message,
    });
    toast.error(error.response.data.message, {
      position: "top-left",
      autoClose: 5000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
    });
  }
};
